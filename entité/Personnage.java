package entité;

public class Personnage {
	protected int x, dirx, y, diry;

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getDirx() {
		return dirx;
	}

	public void setDirx(int dirx) {
		this.dirx = dirx;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getDiry() {
		return diry;
	}

	public void setDiry(int diry) {
		this.diry = diry;
	}
}
